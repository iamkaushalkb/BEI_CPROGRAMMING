// Program to Swap Two Numbers Using Functions
#include <stdio.h>
void Swap(int, int);

int main()
{
    int A, B;
    printf("\nPlease Enter the value of A and B\n");
    scanf("%d %d", &A, &B);
    printf("\nBefore Swap: A = %d and A = %d\n", A, B);
    Swap(A, B);
    return 0;
}
void Swap(int A, int B)
{
    int third_var;
    third_var = A;
    A = B;
    B = third_var;
    printf("\nAfter Swapping: A = %d and B = %d\n", A, B);
}
